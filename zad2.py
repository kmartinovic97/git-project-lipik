#2  Kreirajte dvije varijable i dodijelite im vrijednosti True ili False. Ispišite rezultate logičkih operatora
# (and, or, not) za dvije prethodno kreirane varijable.

x = True
y = False

print(f'{x} or {y} = {x or y}')
print(f'{x} and {y} = {x and y}')
print(f'not {x} = { not x}')
print(f'not {y} = { not y}')